def main():
    puzzles = json.loads(PUZZLES_JSON.read_text(encoding='utf-8'))

    by_cat = defaultdict(list)
    for p in puzzles:
        cat = p.get('category', '')
        ans = p.get('answer', '').strip()
        if cat and ans:
            by_cat[cat].append(ans)

    seen = set()
    rows = []

    def push(a, b, score, tag, reason):
        for ans, inp in [(a, b), (b, a)]:
            key = (ans, inp)
            if key in seen:
                continue
            seen.add(key)
            rows.append({
                'answer': ans, 'user_input': inp,
                'score_0_100': str(score),
                'relation_tag': tag, 'reason': reason,
            })

    for cat, pairs in CATEGORY_PAIRS.items():
        for a, b, score, reason in pairs:
            tag = 'same_category_strong' if score >= 50 else ('same_category_mid' if score >= 35 else 'same_category_weak')
            push(a, b, score, tag, reason)

    # Auto-generate more same-category pairs from puzzles
    rng = random.Random(20260515)
    auto_count = 0
    for cat, answers in by_cat.items():
        if len(answers) < 3:
            continue
        unique = list(set(answers))
        rng.shuffle(unique)
        for i in range(min(8, len(unique))):
            for j in range(i + 1, min(i + 4, len(unique))):
                a, b = unique[i], unique[j]
                if (a, b) not in seen:
                    score = rng.randint(30, 55)
                    tag = 'same_category_mid'
                    push(a, b, score, tag, f'同类弱关联：同属{cat}分类(自动)')
                    auto_count += 1

    rng.shuffle(rows)
    for i, row in enumerate(rows, 1):
        row['id'] = str(i)

    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT.open('w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)

    manual_count = sum(1 for r in rows if '自动' not in r['reason'])
    print(f'same_category_graded_v28: {len(rows)} rows (manual={manual_count} auto={auto_count})')

if __name__ == '__main__':
    main()
