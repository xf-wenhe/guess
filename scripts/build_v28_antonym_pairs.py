FIELDNAMES = ['answer', 'user_input', 'score_0_100', 'relation_tag', 'reason']
OUTPUT = Path('data/antonym_pairs_v28.csv')

def main():
    seen = set()
    rows = []
    for a, b, score in ANTONYM_PAIRS:
        for ans, inp in [(a, b), (b, a)]:
            key = (ans, inp)
            if key in seen:
                continue
            seen.add(key)
            rows.append({
                'answer': ans,
                'user_input': inp,
                'score_0_100': "50",
                'relation_tag': 'antonym_mid',
                'reason': f'反义词：语义对立，按 50% 语义相关度训练',
            })

    random.seed(20260515)
    random.shuffle(rows)
    for i, row in enumerate(rows, 1):
        row['id'] = str(i)

    FIELDNAMES_OUT = ['id'] + FIELDNAMES
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT.open('w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES_OUT)
        writer.writeheader()
        writer.writerows(rows)

    print(f'antonym_pairs_v28: {len(rows)} rows (bidirectional)')

if __name__ == '__main__':
    main()
