def main():
    seen = set()
    rows = []

    def push(a, b, score, reason):
        for ans, inp in [(a, b), (b, a)]:
            key = (ans, inp)
            if key in seen:
                continue
            seen.add(key)
            if ans == inp:
                score = max(score, 96)
            if score >= 80:
                tag = 'alias_synonym_high'
            elif score >= 65:
                tag = 'near_synonym_mid'
            else:
                tag = 'related_synonym_low'
            rows.append({
                'answer': ans, 'user_input': inp,
                'score_0_100': str(score),
                'relation_tag': tag, 'reason': reason,
            })

    for a, b, score in SYNONYM_PAIRS:
        push(a, b, score, '同义词/近义词/别名')

    random.seed(20260515)
    random.shuffle(rows)
    for i, row in enumerate(rows, 1):
        row['id'] = str(i)

    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    with OUTPUT.open('w', encoding='utf-8', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)

    print(f'synonym_expansion_v28: {len(rows)} rows')

if __name__ == '__main__':
    main()
